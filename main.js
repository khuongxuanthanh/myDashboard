
// show navbar
function showNavbar() {
  var x = document.getElementById("showMenu");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

// close navbar
function closeNavbar() {
  var x = document.getElementById("showMenu");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}